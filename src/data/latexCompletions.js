export function latexCompletionSource(context) {
  const match = context.matchBefore(/\\[a-zA-Z{}\[\]]*\*?/);
  if (!match || (match.from === match.to && !context.explicit)) return null;

  return {
    from: match.from,
    options: COMMANDS.map(([label, template, detail]) => ({
      label,
      apply: template.includes('${') ? snippet(template) : template,
      detail,
      type: 'keyword',
    })),
    validFor: /^\\[a-zA-Z{}\[\]]*\*?$/,
  };
}
