export function makeLatexCompletionSource(lang = 'es') {
  const detailIdx = lang === 'en' ? 3 : 2;
  return function latexCompletionSource(context) {
    const match = context.matchBefore(/\\[a-zA-Z{}\[\]]*\*?/);
    if (!match || (match.from === match.to && !context.explicit)) return null;
    return {
      from: match.from,
      options: COMMANDS.map(([label, template, ...details]) => ({
        label,
        apply: template.includes('${') ? snippet(template) : template,
        detail: details[detailIdx - 2],
        type: 'keyword',
      })),
      validFor: /^\\[a-zA-Z{}\[\]]*\*?$/,
    };
  };
}
